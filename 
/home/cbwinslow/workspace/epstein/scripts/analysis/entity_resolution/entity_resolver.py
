
#!/usr/bin/env python3
"""
Entity Resolution System for Epstein Data Analysis
"""

import psycopg2
import pandas as pd
import numpy as np
from difflib import SequenceMatcher
import unicodedata
import re
from typing import Dict, List, Tuple, Optional
import json
from datetime import datetime
import logging

logging.basicConfig(level=logging.INFO, format='%(asctime)s - %(levelname)s - %(message)s')
logger = logging.getLogger(__name__)

class EntityResolver:
    def __init__(self, db_config: Dict):
        self.db_config = db_config
        self.conn = None

    def connect(self):
        try:
            self.conn = psycopg2.connect(**self.db_config)
            logger.info("Connected to database")
        except Exception as e:
            logger.error(f"DB connection failed: {e}")
            raise

    def close(self):
        if self.conn:
            self.conn.close()

    @staticmethod
    def normalize_name(name: str) -> str:
        if not name or not isinstance(name, str):
            return ""
        name = name.lower().strip()
        name = unicodedata.normalize('NFKD', name).encode('ASCII', 'ignore').decode('ASCII')
        titles = ['mr\.?', 'mrs\.?', 'ms\.?', 'dr\.?', 'hon\.?', 'rep\.?', 'sen\.?']
        for title in titles:
            name = re.sub(r'^' + title + r'\s+', '', name)
        name = re.sub(r'[^a-z0-9\s\-]', ' ', name)
        name = re.sub(r'\s+', ' ', name)
        return name.strip()

    @staticmethod
    def similarity_score(name1: str, name2: str) -> float:
        norm1 = EntityResolver.normalize_name(name1)
        norm2 = EntityResolver.normalize_name(name2)
        if not norm1 or not norm2:
            return 0.0
        return SequenceMatcher(None, norm1, norm2).ratio()

    def resolve_entities(self, threshold=0.85):
        logger.info("Extracting entities...")

        # Get all unique names from all sources
        query = """
        SELECT DISTINCT bioguide_id, full_name, 'politician' as etype, state, chamber
        FROM house_financial_disclosures WHERE bioguide_id IS NOT NULL
        UNION
        SELECT DISTINCT bioguide_id, full_name, 'politician', state, chamber
        FROM senate_financial_disclosures WHERE bioguide_id IS NOT NULL
        UNION
        SELECT NULL, asset_name, 'company', NULL, NULL
        FROM congress_trading WHERE asset_name IS NOT NULL
        UNION
        SELECT NULL, lobbyist_name, 'lobbyist', NULL, NULL
        FROM lda_filings WHERE lobbyist_name IS NOT NULL
        UNION
        SELECT NULL, client_name, 'lobbying_client', NULL, NULL
        FROM lda_filings WHERE client_name IS NOT NULL
        UNION
        SELECT NULL, name, 'donor', state, NULL
        FROM fec_individual_contributions WHERE name IS NOT NULL AND transaction_amt > 0
        """

        df = pd.read_sql(query, self.conn)
        df = df.dropna(subset=['full_name'])
        df['std_name'] = df['full_name'].apply(self.normalize_name)

        # Group by exact match first
        df['group_id'] = -1
        gid = 0

        for std in df['std_name'].unique():
            mask = df['std_name'] == std
            if mask.sum() > 1:
                df.loc[mask, 'group_id'] = gid
                gid += 1

        ungrouped = df[df['group_id'] == -1].copy()

        # Fuzzy match remaining
        for idx, row in ungrouped.iterrows():
            if row['group_id'] != -1:
                continue
            mask = ungrouped['group_id'] == -1
            sims = ungrouped[mask].apply(
                lambda x: self.similarity_score(row['std_name'], x['std_name']), axis=1)
            matches = sims >= threshold
            if matches.sum() > 0:
                ungrouped.loc[mask, 'group_id'] = gid
                gid += 1

        df.loc[df['group_id'] == -1, 'group_id'] = range(gid, gid + (df['group_id'] == -1).sum())

        logger.info(f"Created {gid} entity groups")
        return df

    def save_resolutions(self, df):
        logger.info("Saving to database...")

        cur = self.conn.cursor()
        cur.execute("""
        CREATE TABLE IF NOT EXISTS entity_resolutions (
            resolution_id SERIAL PRIMARY KEY,
            standardized_name VARCHAR(500),
            entity_type VARCHAR(50),
            bioguide_id VARCHAR(20),
            state VARCHAR(2),
            source_count INTEGER,
            created_at TIMESTAMP DEFAULT NOW()
        );

        CREATE TABLE IF NOT EXISTS entity_raw_names (
            id SERIAL PRIMARY KEY,
            resolution_id INTEGER REFERENCES entity_resolutions(resolution_id),
            raw_name VARCHAR(500),
            entity_type VARCHAR(50),
            source_table VARCHAR(50)
        );

        CREATE INDEX IF NOT EXISTS idx_entity_std ON entity_resolutions(standardized_name);
        CREATE INDEX IF NOT EXISTS idx_raw_resolution ON entity_raw_names(resolution_id);
        """)

        # Insert groups
        for gid in df['group_id'].unique():
            group = df[df['group_id'] == gid]
            std_name = group['std_name'].iloc[0]
            etype = group['etype'].mode()[0] if not group['etype'].mode().empty else 'unknown'

            cur.execute("""
            INSERT INTO entity_resolutions (standardized_name, entity_type, bioguide_id, state, source_count)
            VALUES (%s, %s, %s, %s, %s)
            ON CONFLICT DO NOTHING
            RETURNING resolution_id;
            """, (std_name, etype, None, None, len(group)))

            res = cur.fetchone()
            if res:
                rid = res[0]
            else:
                cur.execute("SELECT resolution_id FROM entity_resolutions WHERE standardized_name = %s", (std_name,))
                rid = cur.fetchone()[0]

            for _, row in group.iterrows():
                cur.execute("""
                INSERT INTO entity_raw_names (resolution_id, raw_name, entity_type, source_table)
                VALUES (%s, %s, %s, %s) ON CONFLICT DO NOTHING
                """, (rid, row['full_name'], row['etype'], 'combined_sources'))

        self.conn.commit()
        cur.close()
        logger.info("Saved resolutions")

def main():
    db_config = {'host': 'localhost', 'database': 'epstein', 'user': 'cbwinslow'}
    resolver = EntityResolver(db_config)
    try:
        resolver.connect()
        df = resolver.resolve_entities(0.85)
        resolver.save_resolutions(df)
        print(f"\nResolved {df['group_id'].nunique()} unique entities from {len(df)} records")
        print(f"\nEntity types:")
        print(df['etype'].value_counts())
    finally:
        resolver.close()

if __name__ == '__main__':
    main()
